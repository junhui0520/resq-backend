  static Future<Map<String, String>> translateAll({
    required Map<String, String> texts,
    required String targetLang,
  }) async {
    if (targetLang == 'en') return Map.from(texts);
    final langMap = _translations[targetLang];
    if (langMap == null) return Map.from(texts);
    return {
      for (final entry in texts.entries)
        entry.key: langMap[entry.key] ?? entry.value,
    };
  }
